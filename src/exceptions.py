class ZeroException(Exception):
    """Исключение для случаев, когда пытаются добавить товар с нулевым/отрицательным количеством."""

    def __init__(self, message: str = "Нельзя добавить товар с нулевым количеством") -> None:
        """
        Инициализирует исключение с пользовательским сообщением.
        Args:
            message: Текст ошибки, который будет показан пользователю/в логах.
        """
        super().__init__(message)
